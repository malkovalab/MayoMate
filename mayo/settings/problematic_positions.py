# find problematic_positions that repeat in the list
# and remove them

def find_duplicates(lst):
    #finds duplicates in a nested list
    lst_duplicates = []
    for i in lst:
        if lst.count(i) > 1:
            if i not in lst_duplicates:
                lst_duplicates.append(i)
    return lst_duplicates

